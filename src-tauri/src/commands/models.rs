#[tauri::command]
pub fn list_models() -> Vec<Model> {
    catalog()
}

#[tauri::command]
pub fn get_platform_models(platform: String) -> Vec<Model> {
    let parsed = serde_json::from_str::<Platform>(&format!("\"{}\"", platform)).ok();
    match parsed {
        Some(target) => catalog()
            .into_iter()
            .filter(|model| model.platform == target)
            .collect(),
        None => vec![],
    }
}
