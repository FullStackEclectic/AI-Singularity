use crate::models::{Model, Platform};

fn model(
    id: &str,
    name: &str,
    platform: Platform,
    context_length: Option<u32>,
    supports_vision: bool,
    supports_tools: bool,
    input_price_per_1m: Option<f64>,
    output_price_per_1m: Option<f64>,
) -> Model {
    Model {
        id: id.to_string(),
        name: name.to_string(),
        platform,
        context_length,
        supports_vision,
        supports_tools,
        input_price_per_1m,
        output_price_per_1m,
        is_available: true,
    }
}

